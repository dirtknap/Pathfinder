
using PathfinderSharpX.Utils;

namespace PathfinderSharpX.Commons
{
    public class Point
    {
        public Point()
        {
            X = 0;
            Y = 0;
        }

        public Point(int x, int y)
        {
            X = x;
            Y = y;
        }

        public int X { get; set; }
        public int Y { get; set; }

        public Point NextPoint(Direction direction, Direction offset = Direction.None)
        {
            return new Point(X + Helpers.DirectionValues(offset).X + Helpers.DirectionValues(direction).X, Y + Helpers.DirectionValues(offset).Y + Helpers.DirectionValues(direction).Y);
        }


        #region Operators

        public static bool operator ==(Point p1, Point p2)
        {
            return p1.X == p2.X && p1.Y == p2.Y;
        }

        public static bool operator !=(Point p1, Point p2)
        {
            return !(p1 == p2);
        }

        public override string ToString()
        {
            return $"X = {X}, Y = {Y}";
        }

        public override bool Equals(object obj)
        {
            if (ReferenceEquals(null, obj)) return false;
            if (ReferenceEquals(this, obj)) return true;
            if (obj.GetType() != this.GetType()) return false;
            return Equals((Point)obj);
        }

        protected bool Equals(Point other)
        {
            return X == other.X && Y == other.Y;
        }

        public override int GetHashCode()
        {
            unchecked
            {
                return (X * 397) ^ Y;
            }
        }
    #endregion
    }
}
